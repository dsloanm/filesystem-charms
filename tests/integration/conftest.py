@pytest.fixture(scope="module")
def test_mount_client() -> Path:
    """Get test-mount-client charm to use for integration tests.

    The `LOCAL_TEST_MOUNT_CLIENT` environment variable must be set to
    the path of the locally-built test-mount-client charm.

    Returns:
        `Path` to the local test-mount-client charm.
    """
    path = os.getenv("LOCAL_TEST_MOUNT_CLIENT")
    if not path:
        raise RuntimeError("TEST_MOUNT_CLIENT_DIR environment variable must be set")

    logger.info("using local `test-mount-client` charm located at %s", path)
    return Path(path)


@dataclass
class Machines:
    mounts_machine_id: int
    storage_machine_id: int


@pytest.fixture(scope="session")
def machines(juju: jubilant.Juju, base: str) -> Machines:
    """Get the preallocated machines that can be used to deploy Juju applications.

    Returns:
        `Machines` instance containing the IDs of the deployed machines.
    """
    storage_machine_id = add_machine(
        juju, "mem=8G cores=4 root-disk=40G virt-type=virtual-machine", base=base
    )
    mounts_machine_id = add_machine(juju, "mem=8G cores=4 virt-type=virtual-machine", base=base)

    return Machines(storage_machine_id=storage_machine_id, mounts_machine_id=mounts_machine_id)
