def pytest_addoption(parser: pytest.Parser) -> None:
    parser.addoption(
        "--charm-base",
        action="store",
        default="ubuntu@26.04",
        help="Charm base version to use for integration tests",
    )
    parser.addoption(
        "--keep-models",
        action="store_true",
        default=False,
        help="Keep temporary Juju models after tests complete",
    )
    parser.addoption(
        "--run-lustre",
        action="store_true",
        default=False,
        help="run Lustre tests (slow)",
    )


def pytest_configure(config):
    config.addinivalue_line("markers", "lustre: marks tests for Lustre")


def pytest_collection_modifyitems(config, items):
    if config.getoption("--run-lustre"):
        # Flag given in cli: do not skip tests
        return
    skip_lustre = pytest.mark.skip(reason="need --run-lustre option to run")
    for item in items:
        if "lustre" in item.keywords:
            item.add_marker(skip_lustre)


@pytest.fixture(scope="session")
def juju(request: pytest.FixtureRequest) -> Iterator[jubilant.Juju]:
    """Create a temporary Juju model for the test session."""
    keep_models = bool(request.config.getoption("--keep-models"))
    with jubilant.temp_model(
        keep=keep_models, config={"update-status-hook-interval": "1m"}
    ) as juju:
        # patch up the LXD profile generated by Juju to disable secureboot
        model_name = juju.model.split(":")[-1]
        lxd_profile = f"juju-{model_name}"
        result = subprocess.check_output(
            ["lxc", "profile", "list", "--all-projects", "--format", "json"], text=True
        )
        profiles = json.loads(result)

        target = next(
            (
                (profile.get("project"), profile.get("name"))
                for profile in profiles
                if profile.get("name").startswith(lxd_profile)
            ),
            None,
        )
        if not target:
            profiles = (f"{p.get('name')} ({p.get('project')})" for p in profiles)
            pytest.fail(
                f"Could not find an LXD project containing the profile '{lxd_profile}'. "
                f"Available profiles: {', '.join(profiles)}"
            )

        cmd = [
            "lxc",
            "profile",
            "--project",
            target[0],
            "set",
            target[1],
        ]
        try:
            subprocess.check_call(cmd + ["boot.mode=uefi-nosecureboot"])
        except subprocess.CalledProcessError:
            logger.warning(
                "Failed to set boot.mode=uefi-nosecureboot for LXD profile '%s'. Falling back to security.secureboot=false",
                target[0],
            )
            subprocess.check_call(cmd + ["security.secureboot=false"])

        juju.wait_timeout = 30 * 60
        yield juju
        if request.session.testsfailed:
            log = juju.debug_log(limit=3000)
            print(log, end="")


